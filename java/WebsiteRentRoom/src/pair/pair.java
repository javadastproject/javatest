package pair;

public class pair<T> {
	private String string;
	
	private T var;
	
	public String getString() {
		return this.string;
	}
	
	public void setString(String string) {
		this.string = string;
	}
	
	public T getVar() {
		return this.var;
	}
	
	public void setVar(T var) {
		this.var = var;
	}
}
